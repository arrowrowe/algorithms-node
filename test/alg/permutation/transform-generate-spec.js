var transformGenerate = require('../../../alg/permutation/transform-generate');
var expect = require('chai').expect;

describe('Transform', () => {
  var T = (n, transforms) => expect(transformGenerate(n)).to.eql(transforms);
  it('generates a transformation for each permutation of 1 to n', () => {
    T(0, [
      []
    ]);
    T(1, [
      []
    ]);
    T(2, [
      [],
      [[0, 1]]
    ]);
    T(3, [
      [],
      [[1, 2]],
      [[0, 1]],
      [[0, 1], [1, 2]],
      [[0, 2]],
      [[0, 2], [1, 2]]
    ]);
    T(4, [
      [],
      [[2, 3]],
      [[1, 2]],
      [[1, 2], [2, 3]],
      [[1, 3]],
      [[1, 3], [2, 3]],
      [[0, 1]],
      [[0, 1], [2, 3]],
      [[0, 1], [1, 2]],
      [[0, 1], [1, 2], [2, 3]],
      [[0, 1], [1, 3]],
      [[0, 1], [1, 3], [2, 3]],
      [[0, 2]],
      [[0, 2], [2, 3]],
      [[0, 2], [1, 2]],
      [[0, 2], [1, 2], [2, 3]],
      [[0, 2], [1, 3]],
      [[0, 2], [1, 3], [2, 3]],
      [[0, 3]],
      [[0, 3], [2, 3]],
      [[0, 3], [1, 2]],
      [[0, 3], [1, 2], [2, 3]],
      [[0, 3], [1, 3]],
      [[0, 3], [1, 3], [2, 3]]
    ]);
  });
});
